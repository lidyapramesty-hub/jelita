// Helper: compute stats from static data
export function computeStats(data: Usaha[]): import('@/types/usaha').StatsData {
    const thirtyDaysAgo = new Date();
    thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);

    const byKecamatan: Record<string, number> = {};
    const byKategori: Record<string, number> = {};
    let recentCount = 0;

    data.forEach((r) => {
        if (r.kecamatan_nama) byKecamatan[r.kecamatan_nama] = (byKecamatan[r.kecamatan_nama] || 0) + 1;
        if (r.kbli_kategori_nama) byKategori[r.kbli_kategori_nama] = (byKategori[r.kbli_kategori_nama] || 0) + 1;
        if (new Date(r.created_at) > thirtyDaysAgo) recentCount++;
    });

    return {
        total: data.length,
        mikro: data.filter((r) => r.kelas_usaha === 'mikro').length,
        kecil: data.filter((r) => r.kelas_usaha === 'kecil').length,
        menengah: data.filter((r) => r.kelas_usaha === 'menengah').length,
        besar: data.filter((r) => r.kelas_usaha === 'besar').length,
        lokal: data.filter((r) => r.cakupan_pasar === 'lokal').length,
        regional: data.filter((r) => r.cakupan_pasar === 'regional').length,
        nasional: data.filter((r) => r.cakupan_pasar === 'nasional').length,
        internasional: data.filter((r) => r.cakupan_pasar === 'internasional').length,
        byKecamatan,
        byKategori,
        recentCount,
    };
}
